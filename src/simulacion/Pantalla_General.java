    public String medio(int numero) {
        String numero_a_texto = Integer.toString(numero);
        int longitud = numero_a_texto.length();
        int inicio = (longitud - 4) / 2;
        if (inicio < 0) {
            inicio = 0;
        }
        String medio = numero_a_texto.substring(inicio, inicio + 4);
        return medio;
    }

