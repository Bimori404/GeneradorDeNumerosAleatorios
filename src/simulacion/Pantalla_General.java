    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
       try {
            int semilla = Integer.parseInt(jTextField19.getText());
            int g = Integer.parseInt(jTextField20.getText());
            int a = Integer.parseInt(jTextField21.getText());
            int b = Integer.parseInt(jTextField22.getText());
            int c = Integer.parseInt(jTextField23.getText());
            if (semilla > 0 && a%2==0 && b%2!=0 && g%1==0){
                int modulo = (int) Math.pow(2, g);
                int valor = 0;
                String arreglo [] = new String [3];
                modelo = borrar_2(modelo);
                for (int i = 0; i < modulo; i++) {
                    int parte1 = (semilla * semilla) * a;                   
                    int parte2 = b*semilla;
                    arreglo [0] = "("+a+" * "+semilla+"^2) + "+b+" * "+semilla+" + "+c+") modulo ("+modulo+")";
                    valor = (parte1+parte2+c) % modulo;
                    arreglo[1] = valor+"";
                    arreglo[2] = Double.parseDouble(valor+"")/Double.parseDouble(modulo-1+"")+"";
                    semilla = valor;
                    modelo.addRow(arreglo);
                }
                jTable1.setModel(modelo);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Ingresa los datos correctamente");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Ingresa solo numeros enteros");
        }
    }//GEN-LAST:event_jButton8ActionPerformed

